package com.javafx_ftpclient.ftp;

import java.io.File;
import java.io.FileNotFoundException;
import java.io.IOException;
import java.util.List;
import org.apache.commons.net.ftp.FTPFile;

/**
 * Interface for FTPOperation class.
 *
 * @author Andrew Malitchuk
 */
public interface FTPOperationImpl {

    /**
     * Change working directory to another.
     *
     * @param dirPath the path of new working directory
     * @throws Exception if an exception occurred
     */
    void changeDirectory(String dirPath) throws Exception;

    /**
     * Change working directory to parent
     *
     * @throws IOException if an input or output exception occurred
     */
    void changeToParentDirectory() throws IOException;

    /**
     * Create new directory in remote ftp server.
     *
     * @param dirPath the path/name of new directory in remote ftp server
     * @throws IOException if an input or output exception occurred
     */
    void createDirectory(String dirPath) throws IOException;

    /**
     * Delete single file.
     *
     * @param path the path of directory or file which is going to be deleted
     * @throws IOException if an input or output exception occurred
     */
    void delete(String path) throws IOException;

    /**
     * Delete multiple files from remote ftp server.
     *
     * @param listOfPath the list of the paths of directories or files which are
     * going to be deleted
     * @throws IOException if an input or output exception occurred
     */
    void delete(List<String> listOfPath) throws IOException;

    /**
     * Gets name of working directory.
     *
     * @return the name of current directory
     * @throws IOException if an input or output exception occurred
     */
    String getCurrentDirPath() throws IOException;

    /**
     * Get available information of chosen file in remote server.
     *
     * @param filePath the path of file on which we want get information on
     * remote ftp server
     * @return FTPFile-object of chosen file
     * @throws IOException if an input or output exception occurred
     */
    FTPFile getFileInformation(String filePath) throws IOException;

    /**
     * Gets all included sub files and subdirectories of chosen directory
     *
     * @param dirPath the path of directory in remote ftp server of which its
     * going to get the list of included subfiles and directories
     * @return the list of subfiles and directories of chosen directory
     * @throws IOException if an input or output exception occurred
     */
    List<FTPFile> getListOfFiles(String dirPath) throws IOException;

    /**
     * Remove multiple files between two remote directories
     *
     * @param curFilesListPath the list of the files path which are going to be
     * renamed or removed in remote File System
     * @param destPath the path of destination directory in remote File System
     * @throws IOException if an input or output exception occurred
     */
    void remove(List<String> curFilesListPath, String destPath) throws IOException;

    /**
     * Rename a remote file. Also can be used for remove single remote file.
     *
     * @param oldPath an old path/name of file which is going to be renamed or
     * removed in remote File System
     * @param newPath a new path/name of file in remote File System
     * @throws IOException if an input or output exception occurred
     */
    void rename(String oldPath, String newPath) throws IOException;

    /**
     * Send FTP command to remote server.
     *
     * @param command FTP command for server
     * @throws IOException If an input or output exception occurred
     */
    void sendCommand(String command) throws IOException;

    /**
     * Stop curent files transfer.
     *
     * @throws IOException if an input or output exception occurred
     */
    void stopTransfer() throws IOException;

    /**
     * Uploading local directory with all sub files and subdirectories to ftp
     * server
     *
     * @param remoteDirPath the path of destination directory of ftp server
     * @param localParentDirPath the path of local directory which is going to
     * be uploaded with all included subfiles and directories
     * @param remoteParentDirPath the path of remote destination directory on
     * the ftp server
     * @throws IOException if an input or output exception occurred
     */
    void uploadDirectory(String remoteDirPath, String localParentDirPath,
            String remoteParentDirPath) throws IOException;

    /**
     * Uploading single local file to remote ftp server with certain destination
     * parent directory path. In this method <code>remoteFilePath</code>
     * parameter may be relative path.
     *
     * @param localFilePath the path of the local file for uploading
     * @param remoteFilePath new path/name of the file in remote directory
     * @param remoteDirPath the path of destination directory of ftp server
     * @throws Exception if an input or output exception occurred
     */
    void uploadFile(String localFilePath, String remoteFilePath,
            String remoteDirPath) throws Exception;

    /**
     * Uploading single local file to remote ftp server without certain
     * destination parent directory path. In this method
     * <code>remoteFilePath</code> parameter must to be full path to file
     *
     * @param localFilePath the path of the local file for uploading
     * @param remoteFilePath new path/name of the file in remote directory
     * @return true if successfully completed, false if not
     * @throws IOException if an input or output exception occurred
     */
    boolean uploadFile(String localFilePath, String remoteFilePath)
            throws IOException;

    /**
     * Uploading multiple files to certain directory path of ftp server.
     *
     * @param localFilePathsList the list of files path for multiple uploading
     * @param remoteDirPath the path of destination directory of ftp server
     * @throws IOException if an input or output exception occurred
     * @throws FileNotFoundException if file not found exception occurred
     */
    void uploadFile(List<File> localFilePathsList, String remoteDirPath)
            throws IOException, FileNotFoundException;

}
